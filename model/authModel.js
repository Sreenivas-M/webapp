const mongoose = require("mongoose");

const Auth = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      trim: true,
      unique: true,
    },
    phone: {
      type: String,
      required: true,
      trim: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
      trim: true,
    },
    role: {
      type: String,
      default: "user",
      enum: ['admin', 'user'],
    },
  },
  {
    timestamps: true,
  }
);

module.exports = mongoose.model("Auth", Auth);
